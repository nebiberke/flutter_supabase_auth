/// An enum that represents the status of the bloc.
///
/// Example usage:
/// ```dart
/// BlocStatus.initial;
/// BlocStatus.loading;
/// BlocStatus.loaded;
/// BlocStatus.error;
/// ```
enum BlocStatus {
  /// The initial status of the bloc.
  initial,

  /// The loading status of the bloc.
  loading,

  /// The loaded status of the bloc.
  loaded,

  /// The error status of the bloc.
  error,
}
